package semicolonNetwork;

import java.util.List;

public class Message {

    private String title;
    private String body;

    public Message(String title, String body) {
        this.title = title;
        this.body = body;
    }

    public void setTitle() {
        title = title;
        
    }

    public String getTitle() {
        return title;
    }

    public void setBody() {
        body = body;
    }

    public String getBody() {
        return body;
    }
}
